// basic Syntax

const month = 3; // March

// key is to open the lock key = months
switch (month) {
  // cases are lock
  case 1: // we can make cases as per we want either string,number or anything else(As per key)
    console.log("Jan");

    break;
  case 2:
    console.log("Feb");

    break;
  case 3:
    console.log("Mar");

    break; // if break is not there then down all code get executed
  case 4:
    console.log("Apr");

    break;
  case 5:
    console.log("May");

    break;
  case 6:
    console.log("June");

    break;
  case 7:
    console.log("July");

    break;
  default:
    console.log("Default case here");

    break;
}
